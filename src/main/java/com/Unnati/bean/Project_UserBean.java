package com.Unnati.bean;

public class Project_UserBean {

	private Integer projectUserId;
	private Integer userId;
	private String firstName;
	private Integer projectId;
	private String title;
	private Integer assignStatusId;
	public Integer getProjectUserId() {
		return projectUserId;
	}
	public void setProjectUserId(Integer projectUserId) {
		this.projectUserId = projectUserId;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public Integer getProjectId() {
		return projectId;
	}
	public void setProjectId(Integer projectId) {
		this.projectId = projectId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Integer getAssignStatusId() {
		return assignStatusId;
	}
	public void setAssignStatusId(Integer assignStatusId) {
		this.assignStatusId = assignStatusId;
	}
	
	
	
}
